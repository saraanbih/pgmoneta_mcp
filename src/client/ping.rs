use super::PgmonetaClient;
use crate::constant::Command;
use serde::Serialize;

#[derive(Serialize, Clone, Debug)]
struct PingRequest;

impl PgmonetaClient {
    pub async fn request_ping(username: &str) -> anyhow::Result<String> {
        Self::forward_request(username, Command::PING, PingRequest {}).await
    }
}
